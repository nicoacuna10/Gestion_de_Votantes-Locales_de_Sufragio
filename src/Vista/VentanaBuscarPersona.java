    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String rut = jTextField1.getText().toUpperCase();
        
        try {
            if(rut.indexOf('-')==-1 || rut.length()<9 || rut.length()>10){
                throw new RutException();
            }
        } catch (RutException e) {
            System.out.println(e.getMessage());
            String rutAux = rut;
            while(rutAux.length()<9 || rutAux.length()<9 || rutAux.indexOf('-')==-1){
                rutAux = JOptionPane.showInputDialog("Ingrese nuevamente el rut");
                if(rut.length()>9 && rut.length()<11)break;
                if(rut.indexOf('-')!=-1)break;
                rut=rutAux;
            }              
        }
        
        String datosPersona = valparaiso.obtenerDatosVotante(rut);
        
        if(datosPersona != null){
            
            String[] arregloDatosPersona = datosPersona.split(",");
            
           jLabel1.setText("DATOS VOTANTE");
           
           jLabel2.setText("NOMBRE:     "+arregloDatosPersona[0]);
           jLabel3.setText("RUT:        "+arregloDatosPersona[1]);
           jLabel4.setText("COMUNA:     "+arregloDatosPersona[2]);
           jLabel5.setText("DIRECCION:      "+arregloDatosPersona[3]);
           jLabel6.setText("HABILITADO(A) PARA VOTAR: SI");
           jLabel7.setText("LOCAL:      "+arregloDatosPersona[5]);
           jLabel8.setText("DIRECCION LOCAL:        "+arregloDatosPersona[6]);
           jLabel9.setText("NUMERO DE MESA:     "+arregloDatosPersona[4]);
           
           jTextField1.setVisible(false);
           jButton1.setVisible(false);
           
        }else{
            
            datosPersona = valparaiso.obtenerDatosNoVotante(rut);
            
            if(datosPersona != null){
                
                String[] arregloDatosPersona = datosPersona.split(",");
                
                jLabel1.setText("DATOS NO VOTANTE");
                
                jLabel2.setText("NOMBRE:    "+arregloDatosPersona[0]);
                jLabel3.setText("RUT    "+arregloDatosPersona[1]);
                jLabel4.setText("COMUNA:    "+arregloDatosPersona[2]);
                jLabel5.setText("DIRECCION:     "+arregloDatosPersona[3]);
                jLabel6.setText("HABILITADO(A) PARA VOTAR: NO");
                jLabel7.setText("RAZON:     "+arregloDatosPersona[4]);
                
                jTextField1.setVisible(false);
                jButton1.setVisible(false);
                jLabel8.setVisible(false);
                jLabel9.setVisible(false);
                
            }else{
                JOptionPane.showMessageDialog(rootPane, "La persona no existe en el registro");
                
                this.setVisible(false);
                this.menu.setVisible(true);
                this.dispose();
            }
                 
        }
    }//GEN-LAST:event_jButton1ActionPerformed

